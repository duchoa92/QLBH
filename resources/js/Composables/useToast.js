import { toast } from 'vue-sonner'

export const useToast = () => {

    /*
    |--------------------------------------------------------------------------
    | Success
    |--------------------------------------------------------------------------
    */

    const success = (message) => {

        toast.success(message)
    }

    /*
    |--------------------------------------------------------------------------
    | Error
    |--------------------------------------------------------------------------
    */

    const error = (message) => {

        toast.error(message)
    }

    /*
    |--------------------------------------------------------------------------
    | Info
    |--------------------------------------------------------------------------
    */

    const info = (message) => {

        toast.info(message)
    }

    /*
    |--------------------------------------------------------------------------
    | Warning
    |--------------------------------------------------------------------------
    */

    const warning = (message) => {

        toast.warning(message)
    }

    /*
    |--------------------------------------------------------------------------
    | Loading
    |--------------------------------------------------------------------------
    */

    const loading = (message) => {

        return toast.loading(message)
    }

    /*
    |--------------------------------------------------------------------------
    | Dismiss
    |--------------------------------------------------------------------------
    */

    const dismiss = (id) => {

        toast.dismiss(id)
    }

    return {

        success,

        error,

        info,

        warning,

        loading,

        dismiss,
    }
}